/**
 * 单目运算，输入加减乘除取余
 */

package com.neuedu.hello;

public class Test03 {
    public static void main(String[] args) {
        int x = 10;
        int y = 3;
        System.out.println(x + y);//加法
        System.out.println(x - y);//减法
        System.out.println(x * y);//乘法
        System.out.println(x / y);//除法
        System.out.println(x % y);//取余
    }
}
